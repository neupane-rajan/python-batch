# comparison operators are the operators that compare two values and return either true or false
"""
== : equal to,
!= : not equal to,
> : greater than,
< : less than,
>= : greater than or equal to, a is greater than b  or equal to b,
<= : less than or equal to a is less than b or equal to b,
a
"""
a = 10
b = 11
print(f"a == b: {a == b}") 
print(f"a != b: {a != b}") 
print(f"a > b: {a > b}")
print(f"a < b: {a < b}")
print(f"a >= b: {10 >= 11}")
print(f"a <= b: {10 <= 11}")